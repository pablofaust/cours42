int		ft_strcmp(char *s1, char *s2)
{
	int		i;

	i = 0;
	while (s1[i] && s2[i])
		i++;
	if (s1[i])
		return (s1[i]);
	if (s2[i])
		return (s2[i] * -1);
	else
	{
		while (*s1 && *s2)
		{
			if (*s1 > *s2)
				return (*s1);
			if (*s1 < *s2)
				return (*s1 - *s2);
			else
			{
				s1++;
				s2++;
			}
		}
		return (0);
	}
}
