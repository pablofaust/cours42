int		match(char *s1, char *s2)
{
	if (*s2 == 0)
		return (0);
	if (*s1 == 0 && *s2 != '*')
		return (0);
	while (*s1 && *s2)
	{
		if (*s2 == '*')
		{
			s2++;
			if (*s2 == 0)
				return (1);
			while (*s1 != *s2)
			{
				if (*s1 == 0)
					return (0);
				else
					s1++;
			}
		}
		if (*s1 != *s2)
			return (0);
		s1++;
		s2++;
	}
	return (1);
}
