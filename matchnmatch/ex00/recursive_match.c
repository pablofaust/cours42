int		match(char *s1, char *s2)
{
	while (*s1 != 0 && *s2 != 0 )
	{
		if (*s2 == '*')
			match(s1, s2 + 1);
		if (*s1 != *s2)
			return (0);
		else
		{
			s1++;
			s2++;
		}
	}
	return (1);
}	
